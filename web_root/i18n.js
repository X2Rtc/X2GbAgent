  function supportedLanguages() {
    return Object.keys(dictionaries);
  }

  function normalizeLang(lang) {
    const short = String(lang || '').toLowerCase().split('-')[0];
    return dictionaries[short] ? short : fallbackLang;
  }

  function getLanguage() {
    return normalizeLang(localStorage.getItem(LANG_STORAGE_KEY) || navigator.language || fallbackLang);
  }

  function setLanguage(lang) {
    const next = normalizeLang(lang);
    localStorage.setItem(LANG_STORAGE_KEY, next);
    return next;
  }

  function t(key, lang) {
    const active = normalizeLang(lang || getLanguage());
    if (dictionaries[active] && Object.prototype.hasOwnProperty.call(dictionaries[active], key)) {
      return dictionaries[active][key];
    }
    if (Object.prototype.hasOwnProperty.call(dictionaries[fallbackLang], key)) {
      return dictionaries[fallbackLang][key];
    }
    return key;
  }

  window.GB_I18N = {
    storageKey: LANG_STORAGE_KEY,
    dictionaries,
    supportedLanguages,
    normalizeLang,
    getLanguage,
    setLanguage,
    t
  };
